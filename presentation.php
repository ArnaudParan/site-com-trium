		<h1>Le Mot Des Organisateurs</h1>
		<p>
			Au nom de l’ensemble des organisateurs nous vous souhaitons la bienvenue à cette 24ème édition du Forum Trium. Nous espérons que ce forum saura faire honneur aux vingt-trois précédents.
		</p>

		<p>
			Etudiants ou diplômés des MINES ParisTech, de l’école des Ponts ParisTech, de l’ENSTA ParisTech et de l’ENSAE ParisTech ou d’ailleurs, ce forum sera une occasion unique pour vous d’approcher et de découvrir quelques 150 entreprises, venues vous représenter un panel d’activités aussi large que varié. Que vous soyez attirés par les domaines de la construction, de l’énergie, de l’environnement, de l’industrie, du transport, de la logistique ou encore ceux de la banque, de l’assurance, des services, de l’audit, du conseil, vous aurez, au cours de cette journée, l’opportunité de rencontrer les acteurs de ces secteurs. Vous aurez donc la possibilité, soit au travers de tables rondes et de conférences, soit en vous déplaçant de stand en stand, d’établir un contact privilégié avec les entreprises et peut être d’obtenir prochainement une proposition de stage, de PFE ou d’emploi. Par ailleurs, vous aurez la chance de pouvoir profiter d’un espace conseil sur lequel seront communiqués des informations et conseils pragmatiques concernant la professionnalisation des étudiants et l’insertion professionnelle des jeunes diplômés, la préparation et la présentation de leurs candidatures aux recruteurs (correction de CV et lettres de motivation ainsi que des conseils de préparation aux entretiens). Tout comme l’année dernière, des PME ont été conviées au Forum pour vous permettre de découvrir aussi le monde de l’entreprenariat.
		</p>

		<p>
			Pour clôturer cette journée, nous aurons le privilège de recevoir Bertrand COLLOMB, président d’honneur de LafargeHolcim,  qui animera la conférence d’honneur qui se déroulera à 17h15.
		</p>

		<p>
			Nous remercions enfin l’ensemble des entreprises présentes aujourd’hui pour leur participation et l’intérêt qu’elles portent à nos étudiants et nous vous souhaitons un excellent Forum Trium.
		</p>

